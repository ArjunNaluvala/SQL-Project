-- ANALYSIS QUERIES
-- 1. Customer Insights
-- Total Unique Customers
SELECT COUNT(DISTINCT CustomerID) AS total_customers 
	   FROM orders;
-- result: 156 customers

-- Top 5 Customers with Highest Number of Orders
SELECT c.Name, COUNT(o.OrderID) AS total_orders
	FROM customers c
JOIN orders o ON c.CustomerID = o.CustomerID
GROUP BY c.Name
ORDER BY total_orders DESC
LIMIT 5;

-- Total and Average Purchase Value per Customer
SELECT c.Name,
	   SUM(od.TotalPrice) AS total_spent,
       AVG(od.TotalPrice) AS avg_spent
	FROM customers c
JOIN orders o ON c.CustomerID = o.CustomerID
JOIN order_details od ON o.OrderID = od.OrderID
GROUP BY c.Name;

-- Top 5 customers by total purchase amount
SELECT c.name,
	   SUM(od.TotalPrice) AS total_spent
	FROM customers c
JOIN orders o ON c.CustomerID = o.CustomerID
JOIN order_details od ON o.OrderID = od.OrderID
GROUP BY c.Name
ORDER BY total_spent DESC
LIMIT 5;

-- 2. Product Performance
-- Number of Products in Each Category
SELECT c.CategoryName, 
	   COUNT(p.ProductID)
	FROM categories c
JOIN products p ON c.CategoryID = p.CategoryID
GROUP BY c.CategoryName;

-- Average Price of Products by Category
SELECT c.CategoryName,
	   AVG(p.Price) AS Avg_Price
	FROM categories c
JOIN products p ON c.CategoryID = p.CategoryID
GROUP BY c.CategoryName;

-- Top 5 Best-Selling Products (by Quantity)
SELECT p.Name, 
	   SUM(od.Quantity) AS total_sold
	FROM products p
JOIN order_details od ON p.ProductID = od.ProductID
GROUP BY p.Name
ORDER BY total_sold DESC
LIMIT 5;

-- Total Revenue Generated by Each Product
SELECT p.Name, 
	   SUM(od.TotalPrice) AS revenue
	FROM products p
	JOIN order_details od ON p.ProductID = od.ProductID
	GROUP BY p.Name
	ORDER BY revenue DESC;
    
-- Variation of Product Sales by Category and Supplier
SELECT c.CategoryName,
       s.SupplierName,
       SUM(od.Quantity) AS Total_Quantity_Sold,
       SUM(od.TotalPrice) AS Total_Revenue
	FROM order_details od
JOIN products p ON od.ProductID = p.ProductID
JOIN categories c ON p.CategoryID = c.CategoryID
JOIN suppliers s ON p.SupplierID = s.SupplierID
GROUP BY c.CategoryName, s.SupplierName
ORDER BY Total_Revenue DESC;

-- 3. Sales Trends
-- Total Number of Orders
SELECT COUNT(*) AS total_orders 
	FROM orders;

-- Average Order Value
SELECT AVG(TotalPrice) AS avg_order_value 
	FROM order_details;

-- Orders Count by Date (Peak Order Dates)
SELECT OrderDate, 
	   COUNT(OrderID) AS orders_count
	FROM orders
GROUP BY OrderDate
ORDER BY orders_count DESC
LIMIT 2;

-- monthly trends in order volume and revenue
SELECT DATE_FORMAT(STR_TO_DATE(OrderDate, '%m/%d/%Y'), '%Y-%m') AS Month,
	   COUNT(DISTINCT o.OrderID) AS Total_Orders,
       SUM(od.TotalPrice) AS Total_Revenue
	FROM orders o
JOIN order_details od ON o.OrderID = od.OrderID
GROUP BY Month
ORDER BY Month;
   
-- Variation in order patterns across weekdays and weekends
SELECT CASE WHEN DAYOFWEEK(STR_TO_DATE(OrderDate, '%m/%d/%Y')) IN (1,7)
			THEN 'Weekend'
			ELSE 'Weekday'
       END AS Day_Type,
	   COUNT(od.OrderID) AS Total_Orders,
	   SUM(od.TotalPrice) AS Total_Revenue
	FROM orders o
JOIN order_details od ON o.OrderID = od.OrderID
GROUP BY Day_Type;

-- 4. Supplier Contribution
-- Total Number of Suppliers
SELECT COUNT(*) AS supplier_count
	from suppliers;

-- Supplier providing most number of products
SELECT SupplierName, 
	   COUNT(ProductID) as Products_count
	FROM suppliers s
JOIN products p ON s.SupplierID = p.SupplierID
GROUP BY SupplierName
ORDER BY products_count DESC
LIMIT 1;

-- Average Price of Products from Each Supplier
SELECT s.SupplierName,
	   AVG(p.Price) AS Avg_Product_Price
	FROM suppliers s
JOIN products p ON s.SupplierID = p.SupplierID
GROUP BY s.SupplierName;

-- Which suppliers contribute the most to total product sales (by revenue)?
SELECT s.SupplierName,
       SUM(od.TotalPrice) AS Total_Revenue,
       ROUND(
			 (SUM(od.TotalPrice) / 
			 (SELECT SUM(TotalPrice) 
				FROM order_details)
			 ) * 100, 2
			) AS Revenue_Percentage
	FROM suppliers s
JOIN products p ON s.SupplierID = p.SupplierID
JOIN order_details od ON p.ProductID = od.ProductID
GROUP BY s.SupplierName
ORDER BY Total_Revenue DESC
LIMIT 1;

-- 5. Employee Performance
