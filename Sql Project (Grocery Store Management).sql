-- Step-1. Database creation
-- CREATE DATABASE
CREATE DATABASE IF NOT EXISTS grocery_store;
USE grocery_store;

-- Step-2. Table creation
DROP TABLE IF EXISTS OrderDetails;
DROP TABLE IF EXISTS Orders;
DROP TABLE IF EXISTS Products;
DROP TABLE IF EXISTS Customers;
DROP TABLE IF EXISTS Store_Employees;
DROP TABLE IF EXISTS Categories;
DROP TABLE IF EXISTS Suppliers;

-- 1. Creating suppliers table
CREATE TABLE suppliers (
    SupplierID TINYINT AUTO_INCREMENT PRIMARY KEY,
    SupplierName VARCHAR(255),
    Address TEXT
);

-- 2. Creating categories table
CREATE TABLE categories (
    CategoryID TINYINT AUTO_INCREMENT PRIMARY KEY,
    CategoryName VARCHAR(255)
);

-- 3. Creating store_employees table
CREATE TABLE store_employees (
    EmployeeID TINYINT AUTO_INCREMENT PRIMARY KEY,
    Name VARCHAR(255),
    HireDate VARCHAR(50)
);

-- 4. Creating customers table
CREATE TABLE customers (
    CustomerID SMALLINT AUTO_INCREMENT PRIMARY KEY,
    Name VARCHAR(255),
    Address TEXT
);

-- 5. Creating products table
CREATE TABLE products (
    ProductID TINYINT AUTO_INCREMENT PRIMARY KEY,
    Name VARCHAR(255),
    SupplierID TINYINT,
    CategoryID TINYINT,
    Price DECIMAL(10,2),
    FOREIGN KEY (SupplierID) REFERENCES suppliers(SupplierID)
		ON UPDATE CASCADE ON DELETE CASCADE, 
    FOREIGN KEY (CategoryID) REFERENCES categories(CategoryID)
		ON UPDATE CASCADE ON DELETE CASCADE
); -- Foreign keys + cascade

-- 6. Creating orders table
CREATE TABLE orders (
    OrderID SMALLINT AUTO_INCREMENT PRIMARY KEY,
    CustomerID SMALLINT,
    EmployeeID TINYINT,
    OrderDate VARCHAR(50),
    FOREIGN KEY (CustomerID) REFERENCES customers(CustomerID)
		ON UPDATE CASCADE ON DELETE CASCADE,
    FOREIGN KEY (EmployeeID) REFERENCES store_employees(EmployeeID)
		ON UPDATE CASCADE ON DELETE CASCADE
); -- Foreign keys + cascade

-- 7. Creating Order_Details table
CREATE TABLE order_details (
    OrderDetailID SMALLINT AUTO_INCREMENT PRIMARY KEY,
    OrderID SMALLINT,
    ProductID TINYINT,
    Quantity TINYINT,
    PriceEach DECIMAL(10,2),
    TotalPrice DECIMAL(10,2),
    FOREIGN KEY (OrderID) REFERENCES orders(OrderID)
		ON UPDATE CASCADE ON DELETE CASCADE,
    FOREIGN KEY (ProductID) REFERENCES products(ProductID)
		ON UPDATE CASCADE ON DELETE CASCADE
); -- Foreign keys + cascade

